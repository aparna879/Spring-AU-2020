package StoredProcedure;

public class student {

	private int id ;
	private String name;
	private String college;
	private int d_id;
	private String dname;
	
	public int get_id() {
		return id;
	}
	
	public void set_id(int id) {
		this.id = id;
	}
	
	public String get_name() {
		return name;
	}
	
	public void set_name(String name) {
		this.name = name;
	}
	
	public String get_college() {
		return college;
	}
	
	public void set_college(String college) {
		this.college = college;
	}
	
	public int get_did() {
		return d_id;
	}
	
	public void set_did(int d_id) {
		this.d_id = d_id;
	}
	
	public String get_dname() {
		return dname;
	}
	
	public void set_dname(String dname) {
		this.dname = dname;
	}

}

